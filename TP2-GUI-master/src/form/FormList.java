    private void table_pasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_pasienMouseClicked
        try {
            int index = table_pasien.getSelectedRow();
            TableModel model = table_pasien.getModel();

            String nik = model.getValueAt(index, 2).toString();

            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM data_pasien WHERE nik=" + nik + "");

            if (rs.next()) {
                fe.show();
                dispose();

                fe.txt_id_pasien.setText(rs.getString("id_pasien"));
                fe.input_nama.setText(rs.getString("nama"));
                fe.input_nik.setText(rs.getString("nik"));
                fe.input_tgl_lahir.setDate(rs.getDate("tgl_lahir"));
                fe.input_alamat.setText(rs.getString("alamat"));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_table_pasienMouseClicked

    private void btn_prevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_prevActionPerformed
        try {
            offset--;
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT id_pasien, nama, nik, DATE_FORMAT(tgl_lahir, '%Y-%b-%d') AS tgl_lahir, alamat FROM data_pasien LIMIT " + offset + ", 1");

            while (rs.next()) {
                Object[] data = {
                    rs.getString("id_pasien"),
                    rs.getString("nama"),
                    rs.getString("nik"),
                    rs.getString("tgl_lahir"),
                    rs.getString("alamat")
                };

                tabModel.addRow(data);
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_prevActionPerformed

