    public int getRowNumber() {
        int numberRow = 0;

        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT COUNT(*) FROM data_pasien");
            while (rs.next()) {
                numberRow = rs.getInt("COUNT(*)");
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        return numberRow;
    }

